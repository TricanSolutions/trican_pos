    public void LoadGRNDetails() {
        try {

            ResultSet rs = model.DB.getData("SELECT\n"
                    + "grn.id,\n"
                    + "grn.grnno,\n"
                    + "grn.date,\n"
                    + "buz_man_id,\n"
                    + "buz_man.fname,\n"
                    + "buz_man.lname,\n"
                    + "buz_man.contact_1,\n"
                    + "grn.discount_opt,\n"
                    + "grn.discount,\n"
                    + "grn.grand_tot,\n"
                    + "`user`.fname,\n"
                    + "`user`.lname,\n"
                    + "branch.`name`,\n"
                    + "payment_reg.`status`,\n"
                    + "payment_rec.due\n"
                    + "FROM\n"
                    + "grn ,\n"
                    + "buz_man ,\n"
                    + "payment_reg ,\n"
                    + "`user` ,\n"
                    + "branch ,\n"
                    + "payment_rec\n"
                    + "WHERE\n"
                    + "grn.buz_man_id = buz_man.id AND\n"
                    + "grn.`status` = '1' AND\n"
                    + "grn.id = payment_reg.section_id AND\n"
                    + "payment_reg.type = 'GRN' AND\n"
                    + "grn.holder_id = `user`.id AND\n"
                    + "grn.branch_id = branch.id AND\n"
                    + "payment_rec.id = (select max(payment_rec.id) from payment_rec where payment_reg.id=payment_rec.payment_reg_id ORDER BY payment_rec.id DESC ) AND\n"
                    + "grn.id ='" + grnid.trim() + "'\n"
                    + "");

            if (rs.next()) {
                lblgrnid.setText(rs.getString(1));
                lblgrnno.setText(rs.getString(2));
                grnno = rs.getString(2);
                lbldate.setText(rs.getString(3));
                lblsupid.setText(rs.getString(4));
                lblsuppliername.setText(rs.getString(5) + " " + rs.getString(6));
                lblsupcontact.setText(rs.getString(7));

                lbldiscountoption.setText(rs.getString(8));
//                if (rs.getString(8).equals("0")) {
//                    lbldiscountoption.setText("%");
//                } else if (rs.getString(8).equals("1")) {
//                    lbldiscountoption.setText("Price");
//                }

                lbldiscount.setText(rs.getString(9));
                lblgrandamount.setText(rs.getString(10));
                lblloguser.setText(rs.getString(11) + " " + rs.getString(12));
                lblbranch.setText(rs.getString(13));

                if (rs.getString(14).equals(ConstantData.Status_PENDING)) {
                    lblpending.setVisible(true);
                    lblcomplete.setVisible(false);
                } else if (rs.getString(14).equals(ConstantData.Status_COMPLETE)) {
                    lblcomplete.setVisible(true);
                     lblpending.setVisible(false);
                }

                LoadDetails();
                Calculations_Finals(rs.getString(8), rs.getDouble(9), Double.parseDouble(txtgrandamount.getText().trim()));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void Calculations_Finals(String opt, double value, double lasttotal) {
        try {

            if (opt.equals("%")) { // %
                if (value < 100) {
//                 
                    lblfinaldiscount.setText("Rs. " + AllCalculations.DiscountforPrecentage(lasttotal, value));
                    lblgrandamount.setText(AllCalculations.DiscountforPrecentage(lasttotal, value));
                } else {

//                    Calculations_Finals();
                }

            } else//Price
             if (value <= lasttotal) {
                    double discoun = 100 - Double.parseDouble(AllCalculations.DiscountforPrice(lasttotal, value));
                    lblfinaldiscount.setText(Format.decimal3.format(discoun) + "%");
                    lblgrandamount.setText(Format.decimal3.format(lasttotal - value));
                } else {

//                    Calculations_Finals();
                }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void LoadDetails() {
        try {
            DefaultTableModel tbl = (DefaultTableModel) tblgrn.getModel();
            tbl.setRowCount(0);

            ResultSet rs = DB.getData("SELECT\n"
                    + "grn_detail.item_id,\n"
                    + "item.`name`,\n"
                    + "serialize.type,\n"
                    + "grn_detail.batch_id,\n"
                    + "serial_details.serialno,\n"
                    + "batch.warranty,\n"
                    + "grn_detail.qty,\n"
                    + "grn_detail.cost_price,\n"
                    + "grn_detail.discount_opt,\n"
                    + "grn_detail.discount,\n"
                    + "grn_detail.total,\n"
                    + "grn_detail.sell_price,\n"
                    + "batch.discount_opt,\n"
                    + "batch.discount,\n"
                    + "grn_detail.lastcost_price,"
                    + "batch.barcode\n"
                    + "FROM\n"
                    + "grn ,\n"
                    + "grn_detail ,\n"
                    + "serial_details ,\n"
                    + "serialize ,\n"
                    + "batch ,\n"
                    + "item\n"
                    + "WHERE\n"
                    + "grn.id = grn_detail.grn_id AND\n"
                    + "grn_detail.item_id = item.id AND\n"
                    + "grn.id = '" + grnid.trim() + "' AND\n"
                    + "grn_detail.batch_id = batch.id AND\n"
                    + "batch.id = serial_details.batch_id AND\n"
                    + "serial_details.serialize_id = serialize.id");

            while (rs.next()) {
                String itemid = rs.getString(1);
                String itemname = rs.getString(2);
                String serialtype = rs.getString(3);
                String batchno = rs.getString(4);
                String serialno = rs.getString(5);
                String warranty = rs.getString(6);
                double qty = rs.getDouble(7);
                double cost = rs.getDouble(8);
                String discounopcost = rs.getString(9);
                double discountcost = rs.getDouble(10);
                double lastcostprice = rs.getDouble(15);
                double amount = rs.getDouble(11);
                double sell = rs.getDouble(12);
                String discounopsell = rs.getString(13);
                double selldiscount = rs.getDouble(14);
                String barcode = rs.getString(16);

                Object arr[] = {itemid, itemname, serialtype, batchno, barcode, serialno, warranty, qty, cost,
                    discounopcost, discountcost, lastcostprice, amount, sell, discounopsell, selldiscount};
                tbl.addRow(arr);

            }

            TableItemAmountandItemCount();
            lblitemcount.setText(String.valueOf(tblgrn.getRowCount()));
            loadBarcodeTable();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void TableItemAmountandItemCount() {
        if (tblgrn.getRowCount() != 0) {

            double grandamount = 0.0;
            for (int i = 0; i < tblgrn.getRowCount(); i++) {

                grandamount += Double.parseDouble(tblgrn.getValueAt(i, 12).toString());

            }
            txtgrandamount.setText(Format.decimal3.format(grandamount));

        }
    }

