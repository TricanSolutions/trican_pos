    private void loadBarcodeTable() {

        try {

            DefaultTableModel tbl = (DefaultTableModel) tblbarcode.getModel();
            tbl.setRowCount(0);

            if (tblgrn.getRowCount() != 0) {

                int num = 1;

                for (int i = 0; i < tblgrn.getRowCount(); i++) {
                    String name = tblgrn.getValueAt(i, 1).toString();
                    String barcode = tblgrn.getValueAt(i, 4).toString();
                    String serial = tblgrn.getValueAt(i, 5).toString();
                    int qty = (int) Double.parseDouble(tblgrn.getValueAt(i, 7).toString());

                    for (int j = 1; j <= qty; j++) {
//                        System.out.println(num + " " + barcode + " " + qty);
                        Object arr[] = {num, name, barcode, serial, qty};
                        tbl.addRow(arr);
                        num++;
                    }

                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void barcodePrint() {

        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

            JRTableModelDataSource datasource = new JRTableModelDataSource(tblbarcode.getModel());
            String reportSource = ConstantData.ReportPATH + "barcode.jrxml";
            Map<String, Object> params = new HashMap<String, Object>();

            params.put("grnid", lblgrnid.getText());
            params.put("grnno", lblgrnno.getText());
            params.put("datetime", lbldate.getText());

            JasperReport jasperReport = JasperCompileManager.compileReport(reportSource);

            JasperPrint jp = JasperFillManager.fillReport(jasperReport, params, datasource);

//            JasperViewer.viewReport(jp, false);
            JDialog jDialog = new JDialog(this, true);
            jDialog.getContentPane().add(new JRViewer(jp));
            jDialog.pack();

            jDialog.setSize(1000, 700);
            jDialog.setLocationRelativeTo(this);

            jDialog.setVisible(true);

//           JasperPrintManager.printReport(jp, true);
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));

        } catch (Exception e) {
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            e.printStackTrace();
        }

    }
