    private void PrintA5Invaoice(String invoiceid, String datetime) {
        try {
            invoicepay.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

            JRTableModelDataSource datasource = new JRTableModelDataSource(tblinvo.getModel());// for table
            String reportSource = ConstantData.ReportPATH + "invoiceA5.jrxml";
            Map<String, Object> params = new HashMap<String, Object>();

            params.put("id", invoiceid);
            params.put("datetime", datetime);
            //------------------------------------------------------

            params.put("branch", ConstantData.branchName);
            params.put("holder", cmbsalesman.getSelectedItem().toString().split("-")[1]);
            params.put("cusname", invoicepay.txtfirstname.getText() + " " + invoicepay.txtlastname.getText());

            if (!invoicepay.txtsupid.getText().equals("1")) {
                params.put("cuscontact", invoicepay.txtfcontact1.getText());
                params.put("pobox", invoicepay.txtPoBox.getText() + ",");
                params.put("st1", invoicepay.txtStreet1.getText() + ",");
                params.put("st2", invoicepay.txtStreet2.getText() + ",");
                params.put("city", invoicepay.cmbCity.getSelectedItem().toString());
            }

            //---------------------------------------------------------------------------
//            params.put("paymethod", invoicepay.cmbpaymethod.getSelectedItem().toString());
            params.put("netamount", Double.parseDouble(txtgrandamount.getText()));
            params.put("op", cmbfinaldiscount.getSelectedItem().toString());
            params.put("discount", Double.parseDouble(txtfinaldiscount.getText()));
            params.put("grandamount", Double.parseDouble(txtgrandfinalamount.getText()));

            JasperReport jasperReport = JasperCompileManager.compileReport(reportSource);

            JasperPrint jp = JasperFillManager.fillReport(jasperReport, params, datasource);

//            JasperViewer.viewReport(jp, false);
            JDialog jDialog = new JDialog(this, true);
            jDialog.getContentPane().add(new JRViewer(jp));
            jDialog.pack();

            jDialog.setSize(1000, 700);
            jDialog.setLocationRelativeTo(this);

            jDialog.setVisible(true);

//           JasperPrintManager.printReport(jp, true);
            invoicepay.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));

        } catch (Exception e) {
            invoicepay.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            e.printStackTrace();
        }

    }

