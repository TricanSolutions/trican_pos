    public void LoadINVOICEDetails() {
        try {

            ResultSet rs = model.DB.getData("SELECT\n"
                    + "invoice.id,\n"
                    + "invoice.date,\n"
                    + "buz_man_id,\n"
                    + "buz_man.fname,\n"
                    + "buz_man.lname,\n"
                    + "buz_man.contact_1,\n"
                    + "invoice.discount_opt,\n"
                    + "invoice.discount,\n"
                    + "invoice.grand_tot,\n"
                    + "`user`.fname,\n"
                    + "`user`.lname,\n"
                    + "branch.`name`,\n"
                    + "payment_reg.`status`,\n"
                    + "payment_rec.due,\n"
                    + "invoice.note,\n"
                    + "buz_man.pobox,\n"
                    + "buz_man.street_1,\n"
                    + "buz_man.street_2,\n"
                    + "buz_man.city\n"
                    + "FROM\n"
                    + "invoice ,\n"
                    + "buz_man ,\n"
                    + "payment_reg ,\n"
                    + "`user` ,\n"
                    + "branch ,\n"
                    + "payment_rec\n"
                    + "WHERE\n"
                    + "invoice.buz_man_id = buz_man.id AND\n"
                    + "invoice.`status` = '1' AND\n"
                    + "invoice.id = payment_reg.section_id AND\n"
                    + "payment_reg.type = 'INVOICE' AND\n"
                    + "invoice.holder_id = `user`.id AND\n"
                    + "invoice.branch_id = branch.id AND\n"
                    + "payment_rec.id = (select max(payment_rec.id) from payment_rec where payment_reg.id=payment_rec.payment_reg_id ORDER BY payment_rec.id DESC ) AND\n"
                    + "invoice.id ='" + invoiceid.trim() + "'\n"
                    + "");

            if (rs.next()) {

                lblinvoiceno.setText(rs.getString(1));
                lbldate.setText(rs.getString(2));
                lblcustid.setText(rs.getString(3));
                lblcustomername.setText(rs.getString(4) + " " + rs.getString(5));
                lblcustcontact.setText(rs.getString(6));

                lbldiscountoption.setText(rs.getString(7));
//            
                lbldiscount.setText(rs.getString(8));
                lblgrandamount.setText(rs.getString(9));
                lblloguser.setText(rs.getString(10) + " " + rs.getString(11));
                lblbranch.setText(rs.getString(12));

                if (rs.getString(13).equals(ConstantData.Status_PENDING)) {
                    lblpending.setVisible(true);
                    lblcomplete.setVisible(false);
                } else if (rs.getString(13).equals(ConstantData.Status_COMPLETE)) {
                    lblcomplete.setVisible(true);
                    lblpending.setVisible(false);
                }

                txtareanote.setText(rs.getString(15));
                lblpobox.setText(rs.getString(16));
                lblstreet1.setText(rs.getString(17));
                lblstreet2.setText(rs.getString(18));
                lblcity.setText(rs.getString(19));

                LoadDetails();
                Calculations_Finals(rs.getString(7), rs.getDouble(8), Double.parseDouble(txtnetamount.getText().trim()));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void Calculations_Finals(String opt, double value, double lasttotal) {
        try {

            if (opt.equals("%")) { // %
                if (value < 100) {
//                 
                    lblfinaldiscount.setText("Rs. " + AllCalculations.DiscountforPrecentage(lasttotal, value));
                    lblgrandamount.setText(AllCalculations.DiscountforPrecentage(lasttotal, value));
                } else {

//                    Calculations_Finals();
                }

            } else//Price
            {
                if (value <= lasttotal) {
                    double discoun = 100 - Double.parseDouble(AllCalculations.DiscountforPrice(lasttotal, value));
                    lblfinaldiscount.setText(Format.decimal3.format(discoun) + "%");
                    lblgrandamount.setText(Format.decimal3.format(lasttotal - value));
                } else {

//                    Calculations_Finals();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void LoadDetails() {
        try {
            DefaultTableModel tbl = (DefaultTableModel) tblinvo.getModel();
            tbl.setRowCount(0);

            ResultSet rs = DB.getData("SELECT\n"
                    + "invoice_detail.item_id,\n"
                    + "item.`name`,\n"
                    + "batch.barcode,\n"
                    + "serialize.type,\n"
                    + "serial_details.serialno,\n"
                    + "invoice_detail.warranty,\n"
                    + "invoice_detail.cost_price,\n"
                    + "invoice_detail.sell_price,\n"
                    + "invoice_detail.qty,\n"
                    + "invoice_detail.discount_opt,\n"
                    + "invoice_detail.discount,\n"
                    + "invoice_detail.last_sell,\n"
                    + "invoice_detail.total\n"
                    + "FROM\n"
                    + "invoice_detail ,\n"
                    + "batch ,\n"
                    + "item ,\n"
                    + "serial_details ,\n"
                    + "serialize\n"
                    + "WHERE\n"
                    + "invoice_detail.invoice_id = '" + invoiceid.trim() + "' AND\n"
                    + "invoice_detail.batch_id = batch.id AND\n"
                    + "invoice_detail.item_id = item.id AND\n"
                    + "invoice_detail.batch_id = serial_details.batch_id AND\n"
                    + "invoice_detail.batch_id = serial_details.batch_id AND\n"
                    + "serial_details.serialize_id = serialize.id");

            while (rs.next()) {
                String itemid = rs.getString(1);
                String itemname = rs.getString(2);

                String barcode = rs.getString(3);
                String serialtype = rs.getString(4);
                String serialno = rs.getString(5);
                String warranty = rs.getString(6);
                double cost = rs.getDouble(7);
                double sell = rs.getDouble(8);
                double qty = rs.getDouble(9);

                String discounopcost = rs.getString(10);
                double discount = rs.getDouble(11);
                double lastprice = rs.getDouble(12);
                double amount = rs.getDouble(13);

                Object arr[] = {itemid, itemname, barcode, serialtype, serialno, warranty, cost, sell, qty, discounopcost, discount, lastprice, amount};
                tbl.addRow(arr);

            }

            TableItemAmountandItemCount();
            lblitemcount.setText(String.valueOf(tblinvo.getRowCount()));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void TableItemAmountandItemCount() {
        if (tblinvo.getRowCount() != 0) {

            double grandamount = 0.0;
            for (int i = 0; i < tblinvo.getRowCount(); i++) {

                grandamount += Double.parseDouble(tblinvo.getValueAt(i, 12).toString());

            }
            txtnetamount.setText(Format.decimal3.format(grandamount));

        }
    }

