    private void PrintA5Invaoice() {
        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

            JRTableModelDataSource datasource = new JRTableModelDataSource(tblinvo.getModel());// for table
            String reportSource = ConstantData.ReportPATH + "invoiceA5.jrxml";
            Map<String, Object> params = new HashMap<String, Object>();

            params.put("id", lblinvoiceno.getText());
            params.put("datetime", lbldate.getText());
            //------------------------------------------------------

            params.put("branch", lblbranch.getText());
            params.put("holder", lblloguser.getText());
            params.put("cusname", lblcustomername.getText());

            if (!lblcustid.getText().equals("1")) {
                params.put("cuscontact", lblcustcontact.getText());
                params.put("pobox", lblpobox.getText() + ",");
                params.put("st1", lblstreet1.getText() + ",");
                params.put("st2", lblstreet2.getText() + ",");
                params.put("city", lblcity.getText());
            }

            //---------------------------------------------------------------------------
//            params.put("paymethod", invoicepay.cmbpaymethod.getSelectedItem().toString());
            params.put("netamount", Double.parseDouble(txtnetamount.getText()));
            params.put("op", lbldiscountoption.getText());
            params.put("discount", Double.parseDouble(lbldiscount.getText()));
            params.put("grandamount", Double.parseDouble(lblgrandamount.getText()));

            JasperReport jasperReport = JasperCompileManager.compileReport(reportSource);

            JasperPrint jp = JasperFillManager.fillReport(jasperReport, params, datasource);

//            JasperViewer.viewReport(jp, false);
            JDialog jDialog = new JDialog(this, true);
            jDialog.getContentPane().add(new JRViewer(jp));
            jDialog.pack();

            jDialog.setSize(1000, 700);
            jDialog.setLocationRelativeTo(this);

            jDialog.setVisible(true);

//           JasperPrintManager.printReport(jp, true);
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));

        } catch (Exception e) {
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            e.printStackTrace();
        }

    }

