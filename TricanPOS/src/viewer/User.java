    private void submit() {
        if (Empty.ComboBox(cmbUserType, "Select User Type!")) {
        } else if (Empty.ComboBox(cmbBranch, "Select Branch!")) {

        } else if (Empty.TxtFieldEmpty(txtFName, "Enter First Name !")) {
        } else if (Empty.TxtFieldEmpty(txtLName, "Enter Last Name !")) {
        } else if (Empty.TxtFieldEmpty(txtNic, "Enter Nic !")) {
            txtNic.grabFocus();
        } else if (Empty.ContactNumberFormatedFeild(txtContact1, "Enter Contact 1 !")) {
            txtContact1.grabFocus();
        } else if (Empty.ComboBox(cmbCity, "Select City !")) {
        } else if (Empty.ComboBox(cmbStatus, "Select Status !")) {
        } else {

            //////////////////////////////get User type id START//////////////////////////////////////////
            int userType_id = 0;
            try {
                ResultSet rs = model.DB.getData("SELECT\n"
                        + "user_type.id\n"
                        + "FROM\n"
                        + "user_type\n"
                        + "WHERE\n"
                        + "user_type.type = '" + cmbUserType.getSelectedItem() + "'");

                if (rs.next()) {
                    userType_id = rs.getInt(1);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            /////////////////////////////////////////////get User type id END////////////////////////////////////////

            //////////////////////////////////////////////////////////////get branch id START//////////////////////////
            int branch_id = 0;
            try {
                ResultSet rs = model.DB.getData("SELECT\n"
                        + "branch.id\n"
                        + "FROM\n"
                        + "branch\n"
                        + "WHERE\n"
                        + "branch.`name` = '" + cmbBranch.getSelectedItem() + "'");

                if (rs.next()) {
                    branch_id = rs.getInt(1);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            /////////////////////////////////get branch id END///////////////////////////////////////

            String fname = txtFName.getText().trim();
            String lname = txtLName.getText().trim();
            String pwd = txtPassword.getText().trim();
            String nic = txtNic.getText().trim();
            String con1 = txtContact1.getText().trim();
            String con2 = txtContact2.getText().trim();
            String pbox = txtPoBox.getText().trim();
            String street1 = txtStreet1.getText().trim();
            String street2 = txtStreet2.getText().trim();
            Object city = cmbCity.getSelectedItem();

            if (txtid.getText().isEmpty()) {
                //save
                int max_id = Integer.parseInt(GetID());
                try {
                    String status = ConstantData.getStatusID(cmbStatus);

                    model.DB.putData("INSERT INTO user(id,user_type_id,branch_id,fname,lname,password,nic,contact_1,contact_2,pobox,street_1,street_2,"
                            + "city,status)values('" + max_id + "','" + userType_id + "','" + branch_id + "','" + fname + "','" + lname + "','" + pwd + "','" + nic + "','" + con1 + "',"
                            + "'" + con2 + "','" + pbox + "','" + street1 + "','" + street2 + "','" + city + "','" + status + "')");

                    Messages.normaljoption("Data saved!");

                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else {
                //update
                try {
                    String status = ConstantData.getStatusID(cmbStatus);
                    model.DB.putData("UPDATE user SET user_type_id='" + userType_id + "',branch_id='" + branch_id + "',fname='" + fname + "',lname='" + lname + "',"
                            + "password='" + pwd + "',nic='" + nic + "',contact_1='" + con1 + "',contact_2='" + con2 + "',pobox='" + pbox + "',street_1='" + street1 + "',"
                            + "street_2='" + street2 + "',city='" + city + "',status='" + status + "' WHERE id='" + txtid.getText() + "'");
                    Messages.normaljoption("Data Updated!");
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

            clear();
            loadtabl();
            cmbUserType.grabFocus();
        }
        new Logs().createLog("User reg", ConstantData.logUserId);
    }

    private String GetID() { // return next new id
        String id = "1";
        try {
            ResultSet rs = model.DB.getData("select max(id) from user");

            if (rs.next()) {

                if (rs.getString(1) != null) {
                    int num = Integer.parseInt(rs.getString(1));
                    num++;
                    id = "" + num;
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return id;
    }

